#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	n;

	n = 1;
	while (argc > 1)
	{
		i = 0;
		while (argv[n][i] != '\0')
		{
			write(1, &argv[n][i], 1);
			i++;
		}
		argc--;
		n++;
		write(1, "\n", 1);
	}
	return (0);
}
